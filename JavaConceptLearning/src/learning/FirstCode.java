/*Java is case sensitive.
 * */
package learning;

public class FirstCode {
	public static void main(String args[]) {
		/*
		 * Public means the main method will be visible outside the class in which it is
		 * declared. main must be public as it will be called outside the class when the
		 * program started. static allows the main to be called without having
		 * instantiate to a particular instance of class. main will be called by JVM
		 * before any objects were created. String args[] is array of instance of class
		 * String. this will used to receive command line argument.
		 */
		System.out.println("Hello World");
	}
}
